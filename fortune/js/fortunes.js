/** 総合鑑定文を合成する */
export function synthesize(cards, topicKey) {
  const topic = TOPICS[topicKey];
  const stars = luckScore(cards);

  // 支配的なスートを求める(同数なら未来のカードを優先)
  const count = {};
  for (const c of cards) count[c.suit] = (count[c.suit] || 0) + 1;
  let domSuit = cards[2].suit;
  let max = 0;
  for (const [s, n] of Object.entries(count)) {
    if (n > max) { max = n; domSuit = s; }
  }
  const suitLine = max >= 2
    ? `三枚のうち${max}枚が${SUITS[domSuit].name}……「${SUITS[domSuit].theme}」が今のお前の物語を貫く柱だ。`
    : `三枚のスートがすべて異なる。運気が多方面から動き始めている証だ。`;

  const advice = TOPIC_SUIT_ADVICE[topicKey][domSuit];
  const futureCard = MEANINGS[cards[2].id];

  return {
    stars,
    text:
      `${suitLine}\n` +
      `${LUCK_LINE[stars]}\n` +
      `${topic.label}の鍵は「${futureCard.keys[0]}」。${advice}\n` +
      `……運命は定まった絵ではない。この卓で視たものは、お前が筆を執るための下絵にすぎぬ。`,
  };
}

