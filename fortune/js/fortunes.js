const CARD_BONUS = {
  "H-9": 3, "H-10": 2, "D-10": 2, "C-10": 2, "C-A": 2, "H-A": 2, "D-9": 1,
  "S-3": -2, "S-9": -2, "H-5": -1, "D-7": -1, "S-2": -1,
};

export function cardId(suit, rank) { return `${suit}-${rank}`; }

export function cardName(suit, rank) {
  return `${SUITS[suit].name}の${rank === "A" ? "エース" : rank}`;
}

/** 52枚のカード定義を返す */
export function buildDeck() {
  const deck = [];
  for (const suit of Object.keys(SUITS)) {
    for (const rank of RANKS) {
      deck.push({ suit, rank, id: cardId(suit, rank), name: cardName(suit, rank) });
    }
  }
  return deck;
}

/** 位置に応じた読み解き文 */
export function readingFor(card, positionKey) {
  const m = MEANINGS[card.id];
  return POSITION_PHRASE[positionKey](m);
}

/** 3枚から運勢(星1〜5)を算出 */
export function luckScore(cards) {
  let total = 0;
  for (const c of cards) {
    total += RANK_VALUE[c.rank] + (CARD_BONUS[c.id] || 0);
  }
  // total はおよそ 9〜44
  if (total >= 34) return 5;
  if (total >= 28) return 4;
  if (total >= 21) return 3;
  if (total >= 15) return 2;
  return 1;
}

